/**
 * Schwierigkeitsgrad des Rezepts, der die erforderlichen Kochkenntnisse widerspiegelt.
 */
export enum Difficulty {
  /** Grundkenntnisse: Einfache Techniken, wenige Zutaten und minimale Zubereitungszeit. Für Anfänger geeignet. */
  EASY,
  /** Fortgeschrittene Kenntnisse: Erfordert etwas Erfahrung, den Umgang mit komplexeren Techniken oder Timing und eine Vielzahl von Zutaten. */
  MEDIUM,
  /** Expertenkenntnisse: Anspruchsvolle Rezepte, die präzise Techniken, fortgeschrittene Ausrüstung oder umfangreiche Vorbereitung und Timing erfordern. */
  HARD
}
